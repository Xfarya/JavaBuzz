package com.example;

public class JavaBuzz {
    public static void main(String[] args) {
        System.out.println("Hello World");
    }

    public static boolean isJavaBuzz(int number) {
        return number % 15 == 0;
    }

    public static boolean isJava(int number) {
        return number % 5 == 0;
    }
}
